/*
Our program used the addition operator, +, to add two
numbers. Write a program that uses the multiplication operator, *, to print
the product instead.
*/

#include <iostream>

using namespace std;

int main() {
    int v1 = 2;
    int v2 = 3;
    cout << "answer = " << v1 * v2 << endl;
    return 0;
}
